jQuery(function ($) {

	// main loading events
	//
	$("form:not(.ui-dialog form)").live("submit", function () {
		$(document).trigger("loading-start");
	}).live("invalid-form", function () {
		$(document).trigger("loading-stop");
	});

	$("a.no-click, a.disabled").live("click", function (e) {
		e.stopImmediatePropagation();
		return false;
	});

	$("a").live("click", function () {
	    if (!this.href) return;
	    if (this.href.substr(0, 1) == "#") return;
	    if (this.target == "_blank") return;
	    if ($(this).is(".no-loading, .no-loading a, .afPagingLink, .afSortingLink")) return;
		$(document).trigger("loading-start");
	});

	// mask password for security
	$("input[type='password']").live("clear", function () {
		var $self = $(this);
		if ($self.val() == $self.data("mask")) {
			$self.val("");
		};
	}).live("restore", function () {
		var $self = $(this);
		if ($self.val() === "" && $self.data("mask")) {
			$self.val($self.data("mask"));
		}
	});

	$("input[type='password']").live("focus", function () {
		$(this).trigger("clear");
		return true;
	}).live("blur", function () {
		$(this).trigger("restore");
		return true;
	});

	// superfish menu
	//
	/*$('.sf-menu').superfish({
	disableHI: false,
	autoArrows: false,
	delay: 10
	});*/

	// spinbox
	//
	$(".spinbox").spinbox();

	// page and dialog toolbars
	//
	$(".toolbar").toolbar();
	$(".category").category().filter(":not(.expanded)").trigger("collapse");

	$("a.cancel-workflow, #logout-menu-item-link").cancelWorkflow();

	$("a.cancel-scenario").cancelScenario();

	//
	// page title line "(for user: Administrator)" functionality
	var timerId;
	$("#page-title-line .for-user-change").hover(function (e) {
		var $self = $(this);
		$self.addClass("expanded");
		if (timerId) {
			clearTimeout(timerId);
		}

		return false;
	}, function (e) {
		var $self = $(this);
		timerId = setTimeout(function () {
			$self.removeClass("expanded");
		}, 1000);
		return false;
	});

	/* Focusing 'autofocus' elements */
	setTimeout(function () {
		$("input[autofocus]").focus();
	}, 100);


	$("a.change-user").click(function () {
		var $self = $(this);
		if ($self.data("submenu")) {
			$self.data("submenu").remove();
			$self.removeData("submenu");
			return false;
		}
		var $submenu = $("<div class='qui_popover qui_enabled qui_menu'><div class='qui_inner'><div class='qui_menu_item qui_enabled qui_default'><div class='qui_inner'><a href='" + $self.attr("href") + "'>" + $self.data("text") + "</a></div></div></div><div class='qui_n'></div><div class='qui_s hidden'></div></div>").insertAfter($self);
		$(document).click(function () {
			$self.removeData("submenu");
			$submenu.remove();
		});

		$submenu.css("left", $self.position().left);
		$submenu.css("top", $self.position().top + $self.height());
		$("a", $submenu).cancelWorkflow($self.attr("href"));

		$self.data("submenu", $submenu);
		return false;
	});

	$(".toolbar-warning-close").click(function () {
	    var $self = $(this);
		$.post($self.attr("href"), null, function (response) {
			if (!response.Result) {
				result.success = false;
				result.errors = Notification.createErrors(response);
			} else {
				$self.closest(".toolbar-warning").remove();
				window.mainVerticalLayout();
			}
		});
		return false;
	});



	//Workaround for checkboxes' validation
	if ($.validator) {
		/*
		var _showErrors = $.validator.prototype.showErrors;
		$.validator.prototype.showErrors = function (errors) {
			_showErrors.apply(this, arguments);
			var $form = $(this.currentForm);
			if ($form) {
				var $dialog = $form.data("dialog");
				if ($dialog) {
					$dialog.normalize();
				}
			}
		};
		*/
		
		if ($.validator.unobtrusive) {
			$.validator.unobtrusive.adapters.add("checkboxrequired", function (options) {
				options.rules["required"] = true;
				if (options.message) {
					options.messages["required"] = options.message;
				}
			});
		    $.validator.addMethod("manual", function(value, element) {
		        return (typeof element._isValid == "undefined" || element._isValid) ? true : false;
		    });
			$.validator.unobtrusive.adapters.add("manual", function (options) {
			    options.rules["manual"] = true;
			    if (options.message) {
			        options.messages["manual"] = options.message;
			    }
			});
		}
	}

	$(".qui_slider").click(function() {
	    var $slider = $(this);
	    if ($slider.is(".qui_disabled")) return false;
	    var $btn = $(".qui_btn_cmd", $slider);
	    
	    if ($slider.is(".qui_slider_off")) {
	        //left to right
	        $btn.css({
	            left: 0,
	            right: "auto"
	        });
	        $btn.animate({ left: $slider.width() - $btn.outerWidth() }, {
	            complete: function () {
	                $slider.removeClass("qui_slider_off").addClass("qui_slider_on");
	                $btn.css(
	                    {
	                        left: "auto",
	                        right: 0
	                    });
	                $("button", $btn).text($slider.data("sliderOffText"));
	                $slider.trigger("slide", [true]);
	            }});
	        
	    } else {
	        //right to left
	        $btn.css({
	            left: $btn.position().left,
	            right: "auto"
	        });
	        $btn.animate({ left: 0 }, {
            complete: function () {
	            $slider.removeClass("qui_slider_on").addClass("qui_slider_off");
                $btn.css({
                    left: 0,
                    right: "auto"
                });
                $("button", $btn).text($slider.data("sliderOnText"));
                $slider.trigger("slide", [false]);
            }});
	    }
        return false;
    });
});

